int	ft_is_prime(int nb)
{
	long	i;
	long	lnb;

	if (nb == 2 || nb == 3)
		return (1);
	else if (nb <= 1 || nb % 2 == 0 || nb % 3 == 0)
		return (0);
	i = 5;
	lnb = nb;
	while (i * i <= lnb)
	{
		if (lnb % i == 0 || lnb % (i + 2) == 0)
			return (0);
		i += 6;
	}
	return (1);
}
